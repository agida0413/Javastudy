import java.util.Iterator;

public class 제어문_반복문1 {

	public static void main(String[] args) {
		System.out.println("=======for========");
		for(int i=1;i<=10;i++) {
			
			System.out.print(i+" ");
			
		}
		System.out.println("\n==========while=========");
		int i=1;
		while(i<=10) {
			
			System.out.print(i+ " ");
			i++;
			
		}
	}
}
